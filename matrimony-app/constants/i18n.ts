export function tf(language: Language, key: TranslationKey, vars: Record<string, string | number>): string {
  let text = t(language, key);
  Object.entries(vars).forEach(([name, value]) => {
    text = text.replace(`{${name}}`, String(value));
  });
  return text;
}
